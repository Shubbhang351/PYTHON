    public static int move(int[] arr, int start, int end, int i){

        if(end - start == 1 || i == arr.length){
            return 0;
        }

        if(getSum(arr, start, i + 1) == getSum(arr, i + 1, end)){
            int a = move(arr, start, i + 1, start) + 1;


            int b = move(arr, i + 1, end, i + 1) + 1;
            
            return Math.max(a, b);
        }
        else{
            return move(arr, start, end, i + 1);
        }  
    }

    public static int getSum(int[] arr, int start, int end){
        int sum = 0;
        for(int i = start;i < end;i++){
            sum += arr[i];
        }
        return sum;
    }

